    getRandomSixLetterWord() {
        let index = Math.floor(Math.random() * this.#sixLetterWords.length);
        return this.#sixLetterWords[index];
    }

    #isValidSubstring(substring, word) {
        var letters = [...word];
        return [...substring].every(x => {
            var index = letters.indexOf(x);
            if (~index) {
                letters.splice(index, 1);
                return true;
            }
        });
    }

    getValidWordArray(word) {
        let result = new Array();
        for (let i in this.#allWords) {
            let wordSet = this.#allWords[i];
            for (let j=0; j<wordSet.length; j++) {
                let substring = wordSet[j];
                if (this.#isValidSubstring(substring, word)) {
                    result.push(substring.toUpperCase());
                }
            }
        }
        return result;
    }

    getValidWordMap(word) {
        let result = new Map();
        for (let i in this.#allWords) {
            let wordSet = this.#allWords[i];
            for (let j=0; j<wordSet.length; j++) {
                let substring = wordSet[j];
                if (this.#isValidSubstring(substring, word)) {
                    let thisLength = substring.length;
                    let tempArray;
                    if (result.has(thisLength)) {
                        tempArray = result.get(thisLength);
                    } else {
                        tempArray = new Array();
                    }
                    tempArray.push(substring.toUpperCase());
                    result.set(substring.length, tempArray);
                }
            }
        }
        return result;
    }

